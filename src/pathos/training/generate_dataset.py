    parser.add_argument("--output", type=Path, default=Path("training_data/emotional.jsonl"))
    parser.add_argument("--count", type=int, default=10, help="Variations per scenario")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--scenarios", type=Path, default=None, help="File with one scenario per line")
    args = parser.parse_args()

    logging.basicConfig(level=logging.INFO)

    scenarios = DEFAULT_SCENARIOS
    if args.scenarios:
        with open(args.scenarios, encoding="utf-8") as f:
            scenarios = [line.strip() for line in f if line.strip()]

    examples = generate_synthetic_states(scenarios, num_per_scenario=args.count, seed=args.seed)
    saved = save_dataset(examples, args.output)
    print(f"Generated {saved} examples → {args.output}")
