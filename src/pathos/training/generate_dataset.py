@dataclass
class DatasetExample:
    """A single training example."""

    prompt: str
    emotional_tokens: str
    valence: float
    arousal: float
    dominance: float
    certainty: float
    intensity: float
    primary_emotion: str
    system_prompt: str = ""

    def to_dict(self) -> dict:
        return {
            "prompt": self.prompt,
            "emotional_tokens": self.emotional_tokens,
            "valence": round(self.valence, 4),
            "arousal": round(self.arousal, 4),
            "dominance": round(self.dominance, 4),
            "certainty": round(self.certainty, 4),
            "intensity": round(self.intensity, 4),
            "primary_emotion": self.primary_emotion,
            "system_prompt": self.system_prompt,
        }


@dataclass
class DatasetConfig:
    """Configuration for dataset generation."""

    scenarios: list[str] = field(default_factory=lambda: list(DEFAULT_SCENARIOS))
    output_path: Path = Path("training_data/emotional.jsonl")
    num_examples: int = 1000
    personality_variations: int = 5  # different Big Five configs
    seed: int = 42


def generate_synthetic_states(
    scenarios: list[str],
    num_per_scenario: int = 10,
    seed: int = 42,
) -> list[DatasetExample]:
    """Generate synthetic emotional states for scenarios.

    This is a FAST generator that doesn't require LLM or full pipeline.
    It uses heuristic keyword-based appraisal to produce realistic states.

    For higher quality, use generate_pipeline_states() which runs the
    full Pathos emotional pipeline (requires async runtime).

    Args:
        scenarios: List of scenario strings.
        num_per_scenario: Variations per scenario (personality + noise).
        seed: Random seed for reproducibility.

    Returns:
        List of DatasetExample objects.
    """
    from pathos.engine.appraiser import appraise_lite
    from pathos.engine.generator import generate_emotion
    from pathos.models.emotion import neutral_state

    rng = random.Random(seed)
    examples: list[DatasetExample] = []

    for scenario in scenarios:
        for i in range(num_per_scenario):
            # Keyword appraisal (fast, no LLM)
            appraisal_result = appraise_lite(scenario)

            # Generate emotion from appraisal vector
            current = neutral_state()
            state = generate_emotion(
                appraisal_result.vector, current, scenario,
                amplification=rng.uniform(-0.1, 0.2),
            )

            # Add small noise for variation
            state.valence += rng.gauss(0, 0.05)
            state.valence = max(-1.0, min(1.0, state.valence))
            state.arousal += rng.gauss(0, 0.03)
            state.arousal = max(0.0, min(1.0, state.arousal))

            # Generate conditioning tokens
            tokens = state_to_tokens(
                state.valence, state.arousal,
                state.dominance, state.certainty,
                state.intensity,
            )

            examples.append(DatasetExample(
                prompt=scenario,
                emotional_tokens=tokens,
                valence=state.valence,
                arousal=state.arousal,
                dominance=state.dominance,
                certainty=state.certainty,
                intensity=state.intensity,
                primary_emotion=state.primary_emotion.value,
            ))

    return examples


def save_dataset(examples: list[DatasetExample], path: Path) -> int:
    """Save dataset to JSONL file.

    Args:
        examples: Dataset examples.
        path: Output file path.

    Returns:
        Number of examples saved.
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for ex in examples:
            f.write(json.dumps(ex.to_dict(), ensure_ascii=False) + "\n")
    logger.info("Saved %d examples to %s", len(examples), path)
    return len(examples)


def load_dataset(path: Path) -> list[DatasetExample]:
    """Load dataset from JSONL file.

    Args:
        path: Input file path.

    Returns:
        List of DatasetExample objects.
    """
    examples: list[DatasetExample] = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            d = json.loads(line.strip())
            examples.append(DatasetExample(**d))
    return examples


