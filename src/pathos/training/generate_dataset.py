"""Dataset Generator — creates emotional dialogue dataset for fine-tuning.

Uses the Pathos emotional pipeline to generate annotated training examples.
Each example is a (prompt, emotional_state, conditioned_response) triplet.

The pipeline processes each prompt through the full emotional system to
produce authentic emotional states, then pairs them with conditioning tokens.

Output format: JSONL with fields:
  - prompt: str (user message)
  - emotional_tokens: str (conditioning token string)
  - valence, arousal, dominance, certainty, intensity: float
  - primary_emotion: str
  - system_prompt: str (behavior modifier generated by Pathos)

Usage:
  python -m pathos.training.generate_dataset --output training_data/emotional.jsonl
  python -m pathos.training.generate_dataset --scenarios scenarios.txt --count 5000
"""

